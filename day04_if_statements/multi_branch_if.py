
number = 100

result = None

if number > 0 : # if number is greater than 0 ===> positive
    result = 'Positive'
elif number < 0 : # if the number is less than 0 ===> negative
   result = 'Negative'
else : # if number is NOT greater than 0 and NOT less than zero ===> Zero
    result = 'Zero'

print(result)


